package com.mycompany.personobject;
public class Person 
{
    //variable declaration
    private String name;
    private int id;
    
    //setter method
    public void setName(String name)
    {
        this.name=name;
    }
    //getter method
    public String getName()
    {
        return name;
    }
    //setter method
    public void setID(int id)
    {
        this.id=id;
    }
    //getter method
    public int getID()
    {
        return id;
    }
}
